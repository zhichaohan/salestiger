export const createProduct = (
  params,
  successCallback
) => {
  fetch(`/api/v1/products`, {
      method: 'POST',
      headers: {
          "Content-Type": "application/json",
      },
      body: JSON.stringify(params)
  })
  .then(response => response.json())
  .then(data => {
    successCallback(data);
  })
}
